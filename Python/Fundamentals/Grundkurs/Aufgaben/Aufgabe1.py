# Ein Unternehmen hat 50 Mitarbeiter. Überprüfe, ob das Unternehmen mehr als 40 Mitarbeiter hat. 
# Wenn ja, gib aus, dass das Unternehmen „eine große Firma“ ist. Andernfalls gib aus, 
# dass es „eine kleine Firma“ ist.

mitarbeiter = 50
if (mitarbeiter > 40):
    print("Das Unternehmen ist eine große Firma!")
else:
    print("Das Unternehmen ist eine kleine Firma")